  getBuilding(id) { return this.buildings.find(b => b.id === id); },
  getTile(id) { return this.tiles.find(t => t.id === id); },
  getProducer(id) { return this.getBuilding(id) || this.getTile(id); },
  getBuildingsByTier(tier) { return this.buildings.filter(b => b.tier === tier); },
  getProducersOf(resourceId) {
    const ids = this.producers[resourceId] || [];
    return ids.map(id => this.getProducer(id)).filter(Boolean);
  },
  getResourceName(id) { return (this.resources[id] || {}).name || id; },
  // Get full production chain for a building (recursive)
  getProductionChain(buildingId, visited = new Set()) {
    if (visited.has(buildingId)) return null;
    visited.add(buildingId);
    const building = this.getProducer(buildingId);
    if (!building) return null;
    const chain = { ...building, children: [] };
    if (building.inputs) {
      for (const [resId, amount] of Object.entries(building.inputs)) {
        const producers = this.getProducersOf(resId);
        chain.children.push({
          resourceId: resId,
          resourceName: this.getResourceName(resId),
          amountPerIteration: amount,
          producers: producers.map(p => ({
            ...p,
            ratio: building.consumePerMinute[resId] / p.producePerMinute,
            subChain: this.getProductionChain(p.id, new Set(visited))
          }))
        });
      }
    }
    return chain;
  },
